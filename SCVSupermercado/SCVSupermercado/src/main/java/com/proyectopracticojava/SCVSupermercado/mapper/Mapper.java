package com.proyectopracticojava.SCVSupermercado.mapper;

import com.proyectopracticojava.SCVSupermercado.dto.DetalleVentaDTO;
import com.proyectopracticojava.SCVSupermercado.dto.ProductoDTO;
import com.proyectopracticojava.SCVSupermercado.dto.SucursalDTO;
import com.proyectopracticojava.SCVSupermercado.dto.VentaDTO;
import com.proyectopracticojava.SCVSupermercado.model.DetalleVenta;
import com.proyectopracticojava.SCVSupermercado.model.Producto;
import com.proyectopracticojava.SCVSupermercado.model.Sucursal;
import com.proyectopracticojava.SCVSupermercado.model.Venta;

import java.util.stream.Collectors;

public class Mapper {

    //Mapeo de Producto a ProductoDTO

    public static ProductoDTO toDTO(Producto p) {
        if (p == null) return null;

        return ProductoDTO.builder()
                .id(p.getId())
                .nombre(p.getNombre())
                .categoria(p.getCategoria())
                .precio(p.getPrecio())
                .build();

    }
    //Mapeo de Venta a VentaDTO
public static VentaDTO toDTO(Venta venta) {
        if (venta == null) return null;

        var detalle = venta.getDetalle().stream().map(det ->
                DetalleVentaDTO.builder()
                        .id(det.getProducto().getId())
                        .nombreProducto(det.getProducto().getNombre())
                        .cantidad(det.getCantidad())
                        .precio(det.getPrecio())
                        .subtotal(det.getPrecio() * det.getCantidad())
                        .build()


        ).collect(Collectors.toList());

        var total = detalle.stream()
                .map(DetalleVentaDTO::getSubtotal)
                .reduce(0.0, Double::sum);

        return VentaDTO.builder()
                .id(venta.getId())
                .fecha(venta.getFecha())
                .idSucursal(venta.getSucursal().getId())
                .estado(venta.getEstado())
                .detalle(detalle)
                .total(total)
                .build();


}


    //Mapeo de Surcusal a SucursalDTO
    public static SucursalDTO toDTO(Sucursal s) {
        if (s == null) return null;

        return SucursalDTO.builder()
                .id(s.getId())
                .nombre(s.getNombre())
                .direccion(s.getDireccion())
                .build();

    }

}
